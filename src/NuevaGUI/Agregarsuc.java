    private void clavesucKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_clavesucKeyPressed
        // TODO add your handling code here:
        if(evt.getKeyChar()=='\n')
         {
             if(clavesuc.getText().length()>0)
             {
                 if (clavesuc.getText().charAt(0) != 'S') 
                 {
                     clavesuc.setText("S" + clavesuc.getText());
                 }
                 nombresuc.setEnabled(true);
                 nombresuc.requestFocus();
             }
             else
             {
                JOptionPane.showMessageDialog(this, "CAMPO VACIO", "ADVERTENCIA", JOptionPane.WARNING_MESSAGE);
             }
             
         }
    }//GEN-LAST:event_clavesucKeyPressed

