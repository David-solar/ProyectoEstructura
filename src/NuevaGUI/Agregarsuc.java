    private void agregar()
    {
        fecha=dia.getSelectedItem().toString()+"/"+mes.getSelectedItem().toString()+"/"+año.getSelectedItem().toString();
        Sucursal suc=new Sucursal(nombresuc.getText(),fecha,ubicacion.getText());
        Nodo nodsuc=new Nodo(suc,clavesuc.getText());
        BuscaFranq.aux1.setOtro(Principal.lista.insertar(nodsuc, BuscaFranq.aux1.getOtro()));
        JOptionPane.showMessageDialog(this, "AGREGADO","INFORMATIVO",JOptionPane.INFORMATION_MESSAGE);
        Control.contsuc++;
        limpiar();
        int op=JOptionPane.showConfirmDialog(this, "¿DESEA AGREGAR OTRA SUCURSAL?", "AGREGAR SUCURSAL", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if(JOptionPane.OK_OPTION!=op)
        {
            this.hide();
        }
    }
    private void limpiar()
    {
        clavesuc.setText("");
        nombresuc.setText("");
        ubicacion.setText("");
        nombresuc.setEnabled(false);
        ubicacion.setEnabled(false);
        dia.setEnabled(false);
        mes.setEnabled(false);
        año.setEnabled(false);
        agregar.setEnabled(false);
    }
