    private void claveautoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_claveautoKeyTyped
        // TODO add your handling code here:
        Control.contarcadena(claveauto.getText(), 5);
        char c = evt.getKeyChar();
        if(!Control.band)
        {
            evt.consume();
            claveauto.setText("");
            nomprop.setEnabled(false);
        }
        if (((c >= 33 && c<=47) || c>=58) && c != 'S') 
        {
            JOptionPane.showMessageDialog(this, "SOLO VALORES NUMÉRICOS", "ERROR", JOptionPane.WARNING_MESSAGE);
            evt.consume();
        }
    }//GEN-LAST:event_claveautoKeyTyped

