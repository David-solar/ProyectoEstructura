    private void agregar()
    {
        fecha1=diainicio.getSelectedItem().toString()+"/"+mesinicio.getSelectedItem().toString()+"/"+añoinicial.getSelectedItem().toString();
        fecha2=diafin.getSelectedItem().toString()+"/"+mesfinal.getSelectedItem().toString()+"/"+añofinal.getSelectedItem().toString();
        Auto aut=new Auto(nomprop.getText(),fecha1,matricula.getText(),marca.getText(),modelo.getText(),Integer.parseInt(costo.getText()),tiposervicio.getText(),fecha2);
        Nodo nodaut=new Nodo(aut, claveauto.getText());
        BuscaSuc.aux2.setOtro(Principal.lista.insertar(nodaut, BuscaSuc.aux2.getOtro()));
        JOptionPane.showMessageDialog(this, "AGREGADO","INFORMATIVO",JOptionPane.INFORMATION_MESSAGE);
        Control.contauto++;
        limpiar();
        int op=JOptionPane.showConfirmDialog(this, "¿DESEA AGREGAR OTRO AUTOMOVIL?", "AGREGAR AUTO", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
        if(JOptionPane.OK_OPTION!=op)
        {
            this.hide();
        }
    }
